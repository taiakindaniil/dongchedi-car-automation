def brand_name(brand_id: int | None) -> str:
    """Return a display-friendly brand name, falling back to the raw id."""
    if brand_id is None:
        return ""
    return BRANDS.get(brand_id, f"id={brand_id}")


def city_name(city_code: int | None) -> str:
    if city_code is None:
        return "вся страна"
    return CITIES.get(city_code, f"GB{city_code}")


def city_display_ru(city_name_cn: str | None, city_code: int | None) -> str:
    """Prefer Russian from Chinese API city name; else GB code table; else nationwide."""
    if city_name_cn:
        s = city_name_cn.strip()
        if not s:
            return city_name(city_code)
        ru = CITY_CN_TO_RU.get(s)
        if ru is not None:
            return ru
        if s.endswith("市") and len(s) > 1:
            ru = CITY_CN_TO_RU.get(s[:-1])
            if ru is not None:
                return ru
    if city_code is not None:
        return city_name(city_code)
    if city_name_cn and (t := city_name_cn.strip()):
        return t
    return city_name(None)
